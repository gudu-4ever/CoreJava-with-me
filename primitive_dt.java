public class primitive_dt {
    public static void main(String[] args) {
       int a =143;
       System.err.println("Integer type "+a); 
       byte b =127;
       System.err.println("Byte type "+b); 
       short s =10000;
       System.err.println("Short type "+s);
       long l =143143143143143L;
       System.err.println("Long type "+l);
       double d =312.678909875773453;
       System.err.println("Double type "+d); 
       char c='M';
       System.out.println("Character type "+c);
       boolean isAdult=true;
       System.out.println(isAdult);

    }
}
