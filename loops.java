public class loops {
    public static void main(String[] args) {
        //While Loop
        int i=100;
        while(i>0){
            System.out.println(i);
            i--;
        }
            System.out.println("BARRIER");

        //for loop
        for(int j=0;j<=100;j++){
            System.out.println(j);
        }
        
        System.out.println("BARRIER");

        //Do while loop
        int k=10;
        do{
            System.out.println(k);
            k--;
        }while(k>=0);
    }
}
